-- Philippine Payroll System Database Schema
-- PostgreSQL Database

-- Create schema
CREATE SCHEMA IF NOT EXISTS payroll;

-- Set search path
SET search_path TO payroll, public;

-- Enable UUID extension
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ============================================
-- EMPLOYEE MANAGEMENT
-- ============================================

CREATE TABLE employees (
    employee_id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    employee_number VARCHAR(20) UNIQUE NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    middle_name VARCHAR(100),
    last_name VARCHAR(100) NOT NULL,
    suffix VARCHAR(10),
    date_of_birth DATE NOT NULL,
    gender CHAR(1) CHECK (gender IN ('M', 'F')),
    civil_status VARCHAR(20),
    
    -- Contact Information
    email VARCHAR(100),
    mobile_number VARCHAR(20),
    address TEXT,
    city VARCHAR(100),
    province VARCHAR(100),
    postal_code VARCHAR(10),
    
    -- Employment Details
    date_hired DATE NOT NULL,
    date_regularized DATE,
    date_separated DATE,
    employment_status VARCHAR(20) CHECK (employment_status IN ('Active', 'Resigned', 'Terminated', 'Retired')),
    employee_type VARCHAR(20) CHECK (employee_type IN ('Regular', 'Probationary', 'Project-Based', 'Contractual')),
    
    -- Government IDs
    sss_number VARCHAR(20),
    philhealth_number VARCHAR(20),
    pagibig_number VARCHAR(20),
    tin_number VARCHAR(20),
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID,
    updated_by UUID
);

CREATE INDEX idx_employees_status ON employees(employment_status);
CREATE INDEX idx_employees_number ON employees(employee_number);
CREATE INDEX idx_employees_tin ON employees(tin_number);

-- ============================================
-- ORGANIZATION STRUCTURE
-- ============================================

CREATE TABLE departments (
    department_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    department_code VARCHAR(20) UNIQUE NOT NULL,
    department_name VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE positions (
    position_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    position_code VARCHAR(20) UNIQUE NOT NULL,
    position_title VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE employee_assignments (
    assignment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    department_id INTEGER REFERENCES departments(department_id),
    position_id INTEGER REFERENCES positions(position_id),
    is_primary BOOLEAN DEFAULT TRUE,
    effective_date DATE NOT NULL,
    end_date DATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- COMPENSATION AND BENEFITS
-- ============================================

CREATE TABLE salary_grades (
    salary_grade_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    grade_code VARCHAR(20) UNIQUE NOT NULL,
    grade_name VARCHAR(100) NOT NULL,
    min_salary NUMERIC(15, 2) NOT NULL,
    max_salary NUMERIC(15, 2) NOT NULL,
    is_active BOOLEAN DEFAULT TRUE
);

CREATE TABLE employee_compensation (
    compensation_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    salary_grade_id INTEGER REFERENCES salary_grades(salary_grade_id),
    
    -- Basic Pay
    basic_salary NUMERIC(15, 2) NOT NULL,
    daily_rate NUMERIC(15, 2),
    hourly_rate NUMERIC(15, 2),
    
    -- Pay Frequency
    pay_frequency VARCHAR(20) CHECK (pay_frequency IN ('Monthly', 'Semi-Monthly', 'Weekly', 'Daily')),
    
    -- Other Regular Pay
    cola NUMERIC(15, 2) DEFAULT 0, -- Cost of Living Allowance
    
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID
);

CREATE TABLE allowances (
    allowance_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    allowance_code VARCHAR(20) UNIQUE NOT NULL,
    allowance_name VARCHAR(100) NOT NULL,
    description TEXT,
    is_taxable BOOLEAN DEFAULT TRUE,
    is_active BOOLEAN DEFAULT TRUE
);

CREATE TABLE employee_allowances (
    employee_allowance_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    allowance_id INTEGER REFERENCES allowances(allowance_id),
    amount NUMERIC(15, 2) NOT NULL,
    frequency VARCHAR(20) CHECK (frequency IN ('Monthly', 'Per-Payroll', 'Daily', 'One-Time')),
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- ============================================
-- TIME AND ATTENDANCE
-- ============================================

CREATE TABLE attendance (
    attendance_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    attendance_date DATE NOT NULL,
    
    -- Time In/Out
    time_in TIMESTAMP,
    time_out TIMESTAMP,
    
    -- Break Times
    break_out TIMESTAMP,
    break_in TIMESTAMP,
    
    -- Computed Hours
    regular_hours NUMERIC(5, 2) DEFAULT 0,
    overtime_hours NUMERIC(5, 2) DEFAULT 0,
    late_minutes INTEGER DEFAULT 0,
    undertime_minutes INTEGER DEFAULT 0,
    
    -- Status
    is_holiday BOOLEAN DEFAULT FALSE,
    is_rest_day BOOLEAN DEFAULT FALSE,
    is_absent BOOLEAN DEFAULT FALSE,
    
    remarks TEXT,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
    UNIQUE(employee_id, attendance_date)
);

CREATE INDEX idx_attendance_employee_date ON attendance(employee_id, attendance_date);

-- ============================================
-- LEAVE MANAGEMENT
-- ============================================

CREATE TABLE leave_types (
    leave_type_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    leave_code VARCHAR(20) UNIQUE NOT NULL,
    leave_name VARCHAR(100) NOT NULL,
    description TEXT,
    is_paid BOOLEAN DEFAULT TRUE,
    is_active BOOLEAN DEFAULT TRUE
);

CREATE TABLE employee_leaves (
    leave_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    leave_type_id INTEGER REFERENCES leave_types(leave_type_id),
    
    leave_date_from DATE NOT NULL,
    leave_date_to DATE NOT NULL,
    number_of_days NUMERIC(5, 2) NOT NULL,
    
    reason TEXT,
    status VARCHAR(20) CHECK (status IN ('Pending', 'Approved', 'Rejected', 'Cancelled')),
    
    approved_by UUID,
    approved_date TIMESTAMP,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID
);

-- ============================================
-- PHILIPPINE GOVERNMENT CONTRIBUTIONS
-- ============================================

-- SSS (Social Security System) Contribution Table
CREATE TABLE sss_contribution_table (
    sss_table_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    min_salary NUMERIC(15, 2) NOT NULL,
    max_salary NUMERIC(15, 2) NOT NULL,
    employee_contribution NUMERIC(15, 2) NOT NULL,
    employer_contribution NUMERIC(15, 2) NOT NULL,
    total_contribution NUMERIC(15, 2) NOT NULL,
    ec_contribution NUMERIC(15, 2) DEFAULT 0, -- Employees' Compensation
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- PhilHealth Contribution Table
CREATE TABLE philhealth_contribution_table (
    philhealth_table_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    min_salary NUMERIC(15, 2) NOT NULL,
    max_salary NUMERIC(15, 2),
    premium_rate NUMERIC(5, 4) NOT NULL, -- e.g., 0.05 for 5%
    employee_share NUMERIC(5, 4) NOT NULL, -- e.g., 0.50 for 50%
    employer_share NUMERIC(5, 4) NOT NULL, -- e.g., 0.50 for 50%
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- Pag-IBIG Contribution Table
CREATE TABLE pagibig_contribution_table (
    pagibig_table_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    min_salary NUMERIC(15, 2) NOT NULL,
    max_salary NUMERIC(15, 2),
    employee_rate NUMERIC(5, 4) NOT NULL, -- e.g., 0.02 for 2%
    employer_rate NUMERIC(5, 4) NOT NULL, -- e.g., 0.02 for 2%
    max_employee_contribution NUMERIC(15, 2),
    max_employer_contribution NUMERIC(15, 2),
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- Tax (BIR) Withholding Table
CREATE TABLE tax_table (
    tax_table_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    min_compensation NUMERIC(15, 2) NOT NULL,
    max_compensation NUMERIC(15, 2),
    base_tax NUMERIC(15, 2) NOT NULL,
    tax_rate NUMERIC(5, 4) NOT NULL, -- e.g., 0.20 for 20%
    excess_over NUMERIC(15, 2) NOT NULL,
    
    -- Tax period
    period_type VARCHAR(20) CHECK (period_type IN ('Annual', 'Monthly', 'Semi-Monthly', 'Weekly', 'Daily')),
    
    effective_date DATE NOT NULL,
    end_date DATE,
    is_active BOOLEAN DEFAULT TRUE
);

-- ============================================
-- PAYROLL PROCESSING
-- ============================================

CREATE TABLE payroll_periods (
    payroll_period_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period_name VARCHAR(100) NOT NULL,
    period_type VARCHAR(20) CHECK (period_type IN ('Monthly', 'Semi-Monthly', 'Weekly', 'Special')),
    
    period_start_date DATE NOT NULL,
    period_end_date DATE NOT NULL,
    payment_date DATE NOT NULL,
    
    cutoff_date DATE,
    
    status VARCHAR(20) CHECK (status IN ('Draft', 'Processing', 'Completed', 'Posted', 'Cancelled')) DEFAULT 'Draft',
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID,
    processed_at TIMESTAMP,
    processed_by UUID
);

CREATE TABLE payroll_headers (
    payroll_header_id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    payroll_period_id INTEGER REFERENCES payroll_periods(payroll_period_id),
    employee_id UUID REFERENCES employees(employee_id),
    
    -- Basic Information
    basic_salary NUMERIC(15, 2) NOT NULL,
    daily_rate NUMERIC(15, 2),
    hourly_rate NUMERIC(15, 2),
    
    -- Hours Worked
    regular_hours NUMERIC(10, 2) DEFAULT 0,
    overtime_hours NUMERIC(10, 2) DEFAULT 0,
    holiday_hours NUMERIC(10, 2) DEFAULT 0,
    rest_day_hours NUMERIC(10, 2) DEFAULT 0,
    
    -- Days
    days_worked NUMERIC(5, 2) DEFAULT 0,
    days_absent NUMERIC(5, 2) DEFAULT 0,
    days_leave NUMERIC(5, 2) DEFAULT 0,
    
    -- Gross Pay Components
    basic_pay NUMERIC(15, 2) DEFAULT 0,
    overtime_pay NUMERIC(15, 2) DEFAULT 0,
    holiday_pay NUMERIC(15, 2) DEFAULT 0,
    night_differential NUMERIC(15, 2) DEFAULT 0,
    
    -- Other Income
    total_allowances NUMERIC(15, 2) DEFAULT 0,
    total_bonuses NUMERIC(15, 2) DEFAULT 0,
    total_adjustments NUMERIC(15, 2) DEFAULT 0,
    
    -- Gross Calculation
    gross_pay NUMERIC(15, 2) DEFAULT 0,
    
    -- Deductions
    sss_contribution NUMERIC(15, 2) DEFAULT 0,
    philhealth_contribution NUMERIC(15, 2) DEFAULT 0,
    pagibig_contribution NUMERIC(15, 2) DEFAULT 0,
    withholding_tax NUMERIC(15, 2) DEFAULT 0,
    
    total_loans NUMERIC(15, 2) DEFAULT 0,
    total_other_deductions NUMERIC(15, 2) DEFAULT 0,
    
    -- Net Calculation
    total_deductions NUMERIC(15, 2) DEFAULT 0,
    net_pay NUMERIC(15, 2) DEFAULT 0,
    
    -- Status
    status VARCHAR(20) CHECK (status IN ('Draft', 'Computed', 'Approved', 'Paid', 'Cancelled')) DEFAULT 'Draft',
    
    remarks TEXT,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID,
    approved_at TIMESTAMP,
    approved_by UUID
);

CREATE INDEX idx_payroll_header_period ON payroll_headers(payroll_period_id);
CREATE INDEX idx_payroll_header_employee ON payroll_headers(employee_id);

-- Payroll Earnings (Detailed)
CREATE TABLE payroll_earnings (
    payroll_earning_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payroll_header_id UUID REFERENCES payroll_headers(payroll_header_id),
    
    earning_type VARCHAR(50) NOT NULL, -- 'Basic', 'Overtime', 'Holiday', 'Allowance', 'Bonus', etc.
    earning_code VARCHAR(20),
    description VARCHAR(200),
    
    quantity NUMERIC(10, 2), -- hours, days, or units
    rate NUMERIC(15, 2),
    amount NUMERIC(15, 2) NOT NULL,
    
    is_taxable BOOLEAN DEFAULT TRUE,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Payroll Deductions (Detailed)
CREATE TABLE payroll_deductions (
    payroll_deduction_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payroll_header_id UUID REFERENCES payroll_headers(payroll_header_id),
    
    deduction_type VARCHAR(50) NOT NULL, -- 'SSS', 'PhilHealth', 'PagIBIG', 'Tax', 'Loan', 'Other'
    deduction_code VARCHAR(20),
    description VARCHAR(200),
    
    amount NUMERIC(15, 2) NOT NULL,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- LOANS AND DEDUCTIONS
-- ============================================

CREATE TABLE loan_types (
    loan_type_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    loan_code VARCHAR(20) UNIQUE NOT NULL,
    loan_name VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN DEFAULT TRUE
);

CREATE TABLE employee_loans (
    loan_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    loan_type_id INTEGER REFERENCES loan_types(loan_type_id),
    
    loan_amount NUMERIC(15, 2) NOT NULL,
    interest_rate NUMERIC(5, 4) DEFAULT 0,
    number_of_installments INTEGER NOT NULL,
    monthly_amortization NUMERIC(15, 2) NOT NULL,
    
    loan_date DATE NOT NULL,
    first_deduction_date DATE NOT NULL,
    
    total_paid NUMERIC(15, 2) DEFAULT 0,
    balance NUMERIC(15, 2),
    
    status VARCHAR(20) CHECK (status IN ('Active', 'Completed', 'Cancelled')) DEFAULT 'Active',
    
    remarks TEXT,
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by UUID
);

CREATE TABLE loan_payments (
    payment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    loan_id INTEGER REFERENCES employee_loans(loan_id),
    payroll_header_id UUID REFERENCES payroll_headers(payroll_header_id),
    
    payment_date DATE NOT NULL,
    principal_amount NUMERIC(15, 2) NOT NULL,
    interest_amount NUMERIC(15, 2) DEFAULT 0,
    total_amount NUMERIC(15, 2) NOT NULL,
    
    balance_after_payment NUMERIC(15, 2),
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE other_deduction_types (
    deduction_type_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    deduction_code VARCHAR(20) UNIQUE NOT NULL,
    deduction_name VARCHAR(100) NOT NULL,
    description TEXT,
    is_recurring BOOLEAN DEFAULT FALSE,
    is_active BOOLEAN DEFAULT TRUE
);

CREATE TABLE employee_other_deductions (
    other_deduction_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id UUID REFERENCES employees(employee_id),
    deduction_type_id INTEGER REFERENCES other_deduction_types(deduction_type_id),
    
    amount NUMERIC(15, 2) NOT NULL,
    frequency VARCHAR(20) CHECK (frequency IN ('One-Time', 'Monthly', 'Per-Payroll')),
    
    effective_date DATE NOT NULL,
    end_date DATE,
    
    is_active BOOLEAN DEFAULT TRUE,
    remarks TEXT
);

-- ============================================
-- HOLIDAYS
-- ============================================

CREATE TABLE holidays (
    holiday_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    holiday_name VARCHAR(100) NOT NULL,
    holiday_date DATE NOT NULL,
    holiday_type VARCHAR(20) CHECK (holiday_type IN ('Regular', 'Special Non-Working', 'Special Working')) NOT NULL,
    
    -- Premium rates
    regular_day_premium NUMERIC(5, 4) DEFAULT 2.0, -- 200% for regular holiday
    rest_day_premium NUMERIC(5, 4) DEFAULT 2.6, -- 260% for regular holiday on rest day
    
    year INTEGER NOT NULL,
    is_recurring BOOLEAN DEFAULT FALSE, -- If it recurs every year
    
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_holidays_date ON holidays(holiday_date);
CREATE INDEX idx_holidays_year ON holidays(year);

-- ============================================
-- AUDIT TRAIL
-- ============================================

CREATE TABLE audit_logs (
    audit_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_name VARCHAR(100) NOT NULL,
    record_id VARCHAR(50) NOT NULL,
    action VARCHAR(20) CHECK (action IN ('INSERT', 'UPDATE', 'DELETE')) NOT NULL,
    
    old_values JSONB,
    new_values JSONB,
    
    changed_by UUID,
    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    
