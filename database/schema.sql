COMMENT ON TABLE sss_contribution_table IS 'SSS contribution matrix based on monthly salary';
COMMENT ON TABLE philhealth_contribution_table IS 'PhilHealth premium computation table';
COMMENT ON TABLE pagibig_contribution_table IS 'Pag-IBIG contribution computation table';
COMMENT ON TABLE tax_table IS 'BIR withholding tax table (TRAIN Law)';
