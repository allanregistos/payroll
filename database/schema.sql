-- ============================================
-- VIEWS FOR REPORTING
-- ============================================

-- View: Current Employee Compensation
CREATE OR REPLACE VIEW vw_current_employee_compensation AS
SELECT 
    e.employee_id,
    e.employee_number,
    e.first_name,
    e.last_name,
    ec.basic_salary,
    ec.daily_rate,
    ec.hourly_rate,
    ec.pay_frequency,
    ec.cola,
    sg.grade_name as salary_grade
FROM employees e
LEFT JOIN employee_compensation ec ON e.employee_id = ec.employee_id AND ec.is_active = TRUE
LEFT JOIN salary_grades sg ON ec.salary_grade_id = sg.salary_grade_id
WHERE e.employment_status = 'Active';

-- View: Employee with Department and Position
CREATE OR REPLACE VIEW vw_employee_details AS
SELECT 
    e.employee_id,
    e.employee_number,
    e.first_name,
    e.middle_name,
    e.last_name,
    e.email,
    e.mobile_number,
    e.employment_status,
    e.employee_type,
    d.department_name,
    p.position_title,
    e.date_hired,
    e.sss_number,
    e.philhealth_number,
    e.pagibig_number,
    e.tin_number
FROM employees e
LEFT JOIN employee_assignments ea ON e.employee_id = ea.employee_id AND ea.is_primary = TRUE AND ea.end_date IS NULL
LEFT JOIN departments d ON ea.department_id = d.department_id
LEFT JOIN positions p ON ea.position_id = p.position_id;

-- View: Active Loans Summary
CREATE OR REPLACE VIEW vw_active_loans AS
SELECT 
    e.employee_id,
    e.employee_number,
    e.first_name,
    e.last_name,
    lt.loan_name,
    el.loan_amount,
    el.monthly_amortization,
    el.total_paid,
    el.balance,
    el.loan_date,
    el.status
FROM employee_loans el
JOIN employees e ON el.employee_id = e.employee_id
JOIN loan_types lt ON el.loan_type_id = lt.loan_type_id
WHERE el.status = 'Active';

COMMENT ON SCHEMA payroll IS 'Philippine Payroll System Schema';
